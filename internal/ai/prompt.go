// BuildPrompt builds the full prompt for any AI provider.
// All providers must call this function — style is single source of truth.
func BuildPrompt(title, body, source string) string {
	return fmt.Sprintf(
		"%s\n\n---\nЗаголовок оригінал: %s\nТекст оригінал: %s\nДжерело: %s\n\nПерепиши цю новину в моєму стилі. Ліміт 850 символів.",
		styleGuide, title, body, source,
	)
}

